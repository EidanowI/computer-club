-- 2. Проверить использование индексов в запросах
-- Добавьте EXPLAIN перед вашим запросом, например:

-- Пример для проверки использования индекса при поиске пользователя:
EXPLAIN SELECT * FROM user WHERE login = 'test_user';

-- Пример для проверки использования индекса при поиске заказов по дате:
EXPLAIN SELECT * FROM order_tb WHERE date = '2024-01-15';

-- Пример для проверки использования индекса при поиске заказов пользователя:
EXPLAIN SELECT * FROM order_tb WHERE user_id = 1;

-- Пример для проверки использования индекса при проверке прав администратора:
EXPLAIN SELECT * FROM admin WHERE user_id = 1;

